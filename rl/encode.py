# 状态编码 — encode.py
#
# 功能:
#   将 Quoridor::State（C++ 棋盘结构）编码为神经网络能处理的张量。
#   编码方式必须与 C++ RLPlayer::encode_state() 完全一致，
#   否则训练和推理之间会存在"编码鸿沟"导致模型无效。
#
# 编码方案（方案待定，目前为预留）:
#
#   方案 A — 展平向量（适合 MLP）:
#     将 19×19 棋盘展平为 361 维向量，
#     拼接双方位置索引 + 剩余墙数等标量。
#     输入形状: (batch, 365)
#
#   方案 B — 多层特征图（适合 CNN，推荐）:
#     将棋盘编码为多个 9×9 的特征通道，例如:
#       ch 0:  己方位置（=1 的位置）
#       ch 1:  对方位置
#       ch 2:  己方剩余墙数（整层填充）
#       ch 3:  对方剩余墙数（整层填充）
#       ch 4~: 棋盘上的墙分布编码
#     输入形状: (batch, C, 9, 9)
#
# 注意:
#   编码是训练和推理共用的契约，修改此处时必须同步修改
#   player.hpp 中 RLPlayer::encode_state()。
#
# 使用方法:
#   from encode import encode_state
#   tensor = encode_state(state_dict)
