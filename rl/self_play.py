# 自对弈 — self_play.py
#
# 功能:
#   让当前神经网络与自身对弈，生成训练数据。
#   这是强化学习中"数据生产"的环节。
#
# 工作流程:
#   1. 加载当前网络权重
#   2. 创建两个相同的网络实例（或者共享同一份权重）
#   3. 按照 Quoridor 规则交替落子
#   4. 每步记录: (局面编码, 策略目标, 价值目标)
#       - 策略目标: MCTS 搜索后的动作概率分布
#       - 价值目标: 最终对弈结果（赢=+1, 输=-1, 和=0）
#   5. 一局结束后，将整局数据存入 replay buffer
#
# 数据结构:
#   replay buffer 中的每条样本:
#   {
#       "encoded_state":  np.ndarray,    # 编码后的局面
#       "policy_target":  np.ndarray,    # 长度为 164 的概率分布
#       "value_target":   float,         # 对局结果（当前玩家视角）
#       "turn":           int,           # 当前轮到谁
#   }
#
# 与 C++ 的关系:
#   自对弈完全在 Python 侧进行，不依赖 C++ 代码。
#   Python 端会独立实现一套 Quoridor 规则（或在 C++ 编译 Python 模块）。
#   训练完成后只导出权重文件给 C++ 使用。
#
# 使用方法:
#   from self_play import self_play_game
#   game_data = self_play_game(net, mcts_simulations=800)
